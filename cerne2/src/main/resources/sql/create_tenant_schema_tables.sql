    INC_COD BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME TEXT,
    DATA_INICIO_INCUBACAO TEXT,
    EMAIL TEXT,
    STATUS_INCUBACAO VLD_STAUS_INCUBACAO DEFAULT 'INCUBADA',
