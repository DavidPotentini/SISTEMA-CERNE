    PESSOA_COD BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME TEXTNOT NULL,
    EMAIL TEXT UNIQUE NOT NULL,
    CARGO TEXT,
    TIPO_EMPREENDIMENTO VLD_TIPO_EMPREENDIMENTO DAFAULT 'INCUBADORA',
    INC_COD BIGINT REFERENCES INCUBADAS(INC_COD),
    CPF VARCHAR(11) UNIQUE NOT NULL
);

