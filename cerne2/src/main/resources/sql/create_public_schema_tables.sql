CREATE TABLE IF NOT EXISTS TENANTS (
    TNT_COD BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOME_EMPRESA TEXT UNIQUE NOT NULL,
    NOME_SCHEMA VARCHAR(50) UNIQUE NOT NULL
);

CREATE TABLE IF NOT EXISTS USERS(
    USN_COD BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    EMAIL VARCHAR2(255) UNIQUE NOT NULL,
    SENHA TEXT NOT NULL,
    TNT_COD BIGINT REFERENCES TENANTS(TNT_COD)
);